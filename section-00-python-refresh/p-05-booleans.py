# checks for equality
print(5 == 5)

# greater/less than
print(5 > 5)

# not equals operator
print(10 != 10)

# can also compare lists
friends = ["Rolf", "Sarah", "Ruth"]
abroad = ["Rolf", "Sarah", "Ruth"]
print(friends == abroad)

# is: keyword to compare if the two variables are the same object in memory
print(friends is abroad)
